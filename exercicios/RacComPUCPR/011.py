#Aqui, a “esteira” terá os números 4, 6, 8 e 10.  Quando informamos três números dentro do range() ele entende que o primeiro valor é o início, o segundo valor é o fim, e o terceiro valor é o passo. Por isso, este código começa em 4 e vai pulando de dois em dois e para antes de chegar em 12.


for i in range(4,12,2):
    print(i)